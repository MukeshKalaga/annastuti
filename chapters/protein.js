<p>एक आधुनिक योजना <strong>पनीर + दाल + दही + दूध + सोया</strong> के साथ वास्तव में अधिकांश वर्तमान भारतीय आहारों की तुलना में पूर्वजों के खाने के अधिक करीब है। सोया एक 20वीं सदी का शॉर्टकट है — सुविधाजनक और प्रभावी, लेकिन "पारंपरिक" नहीं। एक पूर्णतः पूर्वजों जैसा प्रोटीन दिन सोया रात्रिभोज को <strong>अंकुरित मूँग सलाद + पनीर</strong> से बदल देगा, उन्हीं संख्याओं को 2000 साल पुराने भोजन के माध्यम से प्राप्त करते हुए।</p>

